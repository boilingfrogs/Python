# Lists
# Ordered Collection of objects/items
# Syntax : [ ]
# Mutable

# Operations
# append        ==> Adds an element at the end of list
# insert        ==> Adds an element at a specified position

# remove        ==> Removes an element using name/value
# pop           ==> Removes an element using index

# Indexing / Slicing

# extend       ==> extends the list by the number of elements present in the container

# MUTABLE      ==> List can be modified
# IMMUTABLE    ==> Example: Strings, once defined, it can't be modified

# * can also work as repetition operator